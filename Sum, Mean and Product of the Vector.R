a<- c(12, 14, 16, 17)
b<- c(19, 18, 15, 13)
# Sum of Vectors
c<- sum(a)
d<- sum(b)

cat(" Sum of a is:- ", c)
cat(" Sum of b is:- ",d)
# Mean of Vectors

g<- mean(a)
h<- mean(b)

cat("Mean of a is:- ", g)
cat("Mean of b is:- ", h)
# Product of Vectors

i<- prod(a)
j<- prod(b)

cat("Product of a is:- ", i)
cat("Product of b is:- ", j)
